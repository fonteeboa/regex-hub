// Regex para validar extensões de arquivo comuns (imagens, documentos, texto)
export const fileExtensionRegex = /\.(jpg|jpeg|png|gif|pdf|docx|txt)$/i;
// Regex para validar extensões de imagens (jpg, jpeg, png, gif, bmp, svg)
export const imageFileExtensionRegex = /\.(jpg|jpeg|png|gif|bmp|svg)$/i;
// Regex para validar extensões de documentos (pdf, doc, docx, xls, xlsx, ppt, pptx)
export const documentFileExtensionRegex = /\.(pdf|doc|docx|xls|xlsx|ppt|pptx)$/i;
// Regex para validar extensões de arquivos de texto (txt, csv, log, md)
export const textFileExtensionRegex = /\.(txt|csv|log|md)$/i;
// Regex para validar extensões de arquivos compactados (zip, rar, tar, gz)
export const compressedFileExtensionRegex = /\.(zip|rar|tar|gz|7z)$/i;
// Regex para validar extensões de vídeos (mp4, avi, mkv, mov, flv)
export const videoFileExtensionRegex = /\.(mp4|avi|mkv|mov|flv)$/i;
// Regex para validar extensões de áudios (mp3, wav, flac, aac, ogg)
export const audioFileExtensionRegex = /\.(mp3|wav|flac|aac|ogg)$/i;
// Regex para validar extensões de arquivos executáveis (exe, dll, bat, sh, py)
export const executableFileExtensionRegex = /\.(exe|dll|bat|sh|py)$/i;
// Regex para validar extensões de arquivos de desenvolvimento (js, ts, html, css, json, xml)
export const developmentFileExtensionRegex = /\.(js|ts|html|css|json|xml)$/i;
